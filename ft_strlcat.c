#include "libft.h"

size_t	ft_strlcat(char *dest, const char *src, size_t size)
{
	size_t	i;
	size_t	src_len;

	src_len = ft_strlen(src);
	if (size == 0)
		return (src_len);
	i = 0;
	while (dest[i] && i < size)
		i++;
	if (i < size)
		ft_strlcpy(&dest[i], src, size - i);
	return (size - 1 < i ? size + src_len : src_len + i);
}
