#include "libft.h"

void	ft_lstadd_back(t_list **alst, t_list *new)
{
	t_list	*tmp;

	if (alst)
	{
		if (*alst == NULL)
			*alst = new;
		tmp = ft_lstlast(*(alst));
		if (!tmp)
			return ;
		tmp->next = new;
	}
}
