#include "libft.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	unsigned int	i;
	unsigned int	j;
	char	*str;

	i = 0;
	j = 0;	
	if(!(str = malloc(sizeof(char) * (ft_strlen(s1) + ft_strlen(s2 + 1)))))
		return (NULL);
	while (s1[i])
	{
		str[i] = s1[i];
		i++;
	}
	while (s2[j])
	{
		str[i] = s2[j];
		i++;
		j++;
	}
	str[i] = '\0';
	return(str); 
}